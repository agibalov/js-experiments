/**
 * The ultimate calculator.
 */
export class Calculator {
    /**
     * Adds two numbers.
     * @param {number} a first number to addNumbers
     * @param {number} b second number to addNumbers
     * @returns {number} sum of a and b
     */
    addNumbers(a: number, b: number): number {
        return a + b;
    }

    /**
     * Subtracts two numbers.
     * @param {number} a first number to subtract
     * @param {number} b second number to subtract
     * @returns {number} difference of a and b
     */
    subtractNumbers(a: number, b: number): number {
        return a - b;
    }
}
